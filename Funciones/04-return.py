def suma(a, b):
    resultado = a + b
    return resultado # aca el valor return nos permite devolver la funcion de codigo que se este ejecutando en la funcion
# Entonces como normalmente se realiza con las funciones las llamamos otorgandoles sus argumentos
c = suma(1, 2)
# Al otorgarle los argumentos para obtener el valor de resultado en la funcion lo que se debe hacer es agregar la llamada a la funcion en un variable
# Y esa variable imprimirla en pantalla como se muestra 
print(c)